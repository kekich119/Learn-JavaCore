package org.rebootMethods;

public class SolutionRebootMin {
    public static int min(int a, int b){
        return Math.min(a,b);
    }
    public static double min(double a, double b){
        return Math.min(a,b);
    }
    public static long min(long a, long b){
        return Math.min(a,b);
    }
}
