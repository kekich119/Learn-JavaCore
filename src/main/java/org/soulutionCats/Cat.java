package org.soulutionCats;

public class Cat {
    private String name;
    private int age;
    private int weight;


    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return age;
    }


    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getWeight() {
        return weight;
    }



    public void setName(String name){
        this.name = name;
    }

    public String getName(){
        return name;
    }
}
